// בניית מפת lookup הפוכה: alias -> canonical
const aliasToCanonical = {};
for (const [canonical, aliases] of Object.entries(TEAM_ALIASES)) {
  aliasToCanonical[canonical] = canonical;
  for (const alias of aliases) {
    aliasToCanonical[alias] = canonical;
  }
}

/**
 * נרמול שם קבוצה לשם הקנוני
 * @param {string} teamName
 * @returns {string} שם קנוני
 */
function normalizeTeamName(teamName) {
  if (!teamName) return teamName;
  const trimmed = teamName.trim();

  // חיפוש ישיר
  if (aliasToCanonical[trimmed]) {
    return aliasToCanonical[trimmed];
  }

  // Fallback - fuzzy: חיפוש לפי תחילית/הכלה
  for (const [canonical, aliases] of Object.entries(TEAM_ALIASES)) {
    const allNames = [canonical, ...aliases];
    for (const name of allNames) {
      // אם אחד מכיל את השני (מינימום 4 תווים)
      if (trimmed.length >= 4 && name.length >= 4) {
        if (trimmed.includes(name) || name.includes(trimmed)) {
          return canonical;
        }
      }
    }
  }

  // לא נמצא - מחזיר כמו שהוא
  return trimmed;
}

/**
 * מחזיר את כל השמות הקנוניים
 */
function getAllCanonicalNames() {
  return Object.keys(TEAM_ALIASES);
}

module.exports = { normalizeTeamName, getAllCanonicalNames, TEAM_ALIASES };
